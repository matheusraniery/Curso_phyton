'''
Python = Linguagem de programação
tipo de tipagem = Dinâmica / forte
str -> string -> texto
Strings são textos que estão dentro de aspas.
'''
# Aspas simples ''
print(1,'Hello, word!')
# Aspas duplas ""
print(2,"Hello, word!")
# Escape \ -> ignora o próximo caracter de ser lido mas ele exibe.
print(3,'Hello, \'word!\'')
# r antes da aspas serve para expressões regulares, ele mostra o caractere de barra neste caso. 
print(4,r'Hello, \'word!\'')
# Aspas simples dentrro de aspas duplas pode, ou o contrário também.
print(5,'Matheus "DEV" Raniery')

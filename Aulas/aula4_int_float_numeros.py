# Tipos int e float
# Na programação o . (ponto) sempre será usado para separá os números.
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo
print(11) # int (inteiro)
print(-11) # int (inteiro)
print(0) # int (inteiro)

# float -> Número com ponto flutuante
# Se tem casa decimal é float.
# O tipo float representa qualquer número
# Positivo ou negativo com ponto flutuante.
# Float sem sinal é considerado positivo
print(1.1, 10.11) # float (flutuante)
print(0.0, -1.5) # float (flutuante)

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Matheus')) #str
print(type(123)) #int
print(type(1.1)) #float
print(type('NAME'),type(123),type(1.1))

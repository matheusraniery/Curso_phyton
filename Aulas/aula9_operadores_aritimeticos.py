# Introdução aos operadores aritiméticos (matemática)

adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2 # float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2
print('Divisão inteira', divisao_inteira)

exopnenciacao = 2 ** 10
print('Exponenciação', exopnenciacao)

modulo = 55 %  2 # Resto da divisão
print('Módulo', modulo)

# Pra saber se um número é divisível de outro:

print(10 % 8 == 0) # Se o resto der 0 é divisível
print(16 % 8 == 0) 
print(15 % 8 == 0) 
